# Exception handling is a way of writing code that anticipates exceptions, or errors, and provides alternative
#   code to execute in case of an error. It can be used to handle errors generally or catch specific errors,
#   and even use selective flow of execution based on what kind of error it catches.
#   This way, you can write code that runs into errors (for example, those based on wrong user input) and still
#   have the code complete its entire execution, even if the output is malformed as a result.
